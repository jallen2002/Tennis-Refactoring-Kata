namespace Tennis
{
    class TennisGame : ITennisGame
    {
        private int player1CurrentScore = 0;
        private int player2CurrentScore = 0;
        private string player1Name;
        private string player2Name;

        public TennisGame(string player1Name, string player2Name)
        {
            this.player1Name = player1Name;
            this.player2Name = player2Name;
        }

        public void WonPoint(string playerName)
        {
            if (playerName == "player1")
                player1CurrentScore += 1;
            else
                player2CurrentScore += 1;
        }

        public string GetScore()
        {
            string score = "";
            var tempScore = 0;

            if (player2CurrentScore == player1CurrentScore)
            {
                score = returnTieScore(player2CurrentScore);


                switch (player2CurrentScore)
                {
                    case 0:
                        score = "Love-All";
                        break;
                    case 1:
                        score = "Fifteen-All";
                        break;
                    case 2:
                        score = "Thirty-All";
                        break;
                    default:
                        score = "Deuce";
                        break;

                }
            }
            else if (player1CurrentScore >= 4 || player2CurrentScore >= 4)
            {
                var minusResult = player1CurrentScore - player2CurrentScore;
                if (minusResult == 1) score = "Advantage player1";
                else if (minusResult == -1) score = "Advantage player2";
                else if (minusResult >= 2) score = "Win for player1";
                else score = "Win for player2";
            }
            else
            {
                for (var i = 1; i < 3; i++)
                {
                    if (i == 1) tempScore = player1CurrentScore;
                    else { score += "-"; tempScore = player2CurrentScore; }
                    switch (tempScore)
                    {
                        case 0:
                            score += "Love";
                            break;
                        case 1:
                            score += "Fifteen";
                            break;
                        case 2:
                            score += "Thirty";
                            break;
                        case 3:
                            score += "Forty";
                            break;
                    }
                }
            }
            return score;
        }

        private string returnTieScore(int player2CurrentScore)
        {
            switch (player2CurrentScore)
            {
                case 0:
                    return "Love-All";
                case 1:
                    return "Fifteen-All";
                case 2:
                    return "Thirty-All";
                default:
                    return "Deuce";
            }
        }

    }
}

